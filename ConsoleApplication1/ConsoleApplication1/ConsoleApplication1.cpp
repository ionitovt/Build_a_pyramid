void build(int num){
	int *array;
	if (num % 2 != 0){
		array = (int*)calloc((num * 2), sizeof(int));
		for (int left = num - 1, right = num - 1, temp = num, i; temp > 0; left--, right++, temp--){
			array[left] = temp;
			array[right] = temp;
			if (temp > 10){
				printf("          ");
				for (int j = temp - 1; j > 10; j--){
					printf(" ");
				}
			}
			else{
				for (int i = temp - 1; i > 0; i--){
					printf(" ");
				}
			}
			for (i = 0; array[i] == 0; i++){
				printf(" ");
			}
			for (; array[i] != 0; i++){
				if (array[i] < 10){
					if (array[i - 1] == 10) printf(" ");
					printf("%d ", array[i]);
				}
				else{
					printf("%d", array[i]);
				}
			}
			printf("\n");
		}
	}
	else{
		array = (int*)calloc(((num * 2) + 1), sizeof(int));
		for (int left = num - 1, right = num, temp = num, i; temp > 0; left--, right++, temp--){
			array[left] = temp;
			array[right] = temp;
			if (temp > 10){
				printf("          ");
				for (int j = temp - 1; j > 10; j--){
					printf(" ");
				}
			}
			else{
				for (int i = temp - 1; i > 0; i--){
					printf(" ");
				}
			}
			for (i = 0; array[i] == 0; i++){
				printf(" ");
			}
			for (; array[i] != 0; i++){
				if (array[i] < 10){
					if (array[i - 1] == 10) printf(" ");
					printf("%d ", array[i]);
				}
				else{
					printf("%d", array[i]);
				}
			}
			printf("\n");
		}
	}
	free(array);
	fflush(stdin);
	getchar();
	start();
}